package session5;
//autoboxing
public class WrapperClassEx {
	public static void main(String[] args) {
		int a = Integer.valueOf(50);
		Integer intObj = 50;
		Character charObj = 'A';
		Double dObj = 25.3424;
		
		System.out.println(a);
		System.out.println(intObj);
		System.out.println(charObj);
		System.out.println(dObj);				
		
	}

}
